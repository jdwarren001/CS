#include <iostream>
using namespace std;

// Q2. Write a for loop to add all the multiples of 3 between 1 and 100


int main(int argc, const char * argv[]) {
    
    int a;
    
    
    for (a = 1; a < 100;)
    {
        cout << a << endl << endl;
        a = a + 3;
    
    }
    
    
    
    return 0;
}
